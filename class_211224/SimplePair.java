package class_211224;

public class SimplePair<T> {

	private T first;
	private T second;

	public SimplePair(T first, T second) {
		this.first =first;
		this.second = second;
	}

	public T getFirst() {
		return first;
	}

	public void setFirst(T first) {
		this.first = first;
	}

	public T getSecond() {
		return second;
	}

	public void setSecond(T second) {
		this.second = second;
	}
	
	
	
}
